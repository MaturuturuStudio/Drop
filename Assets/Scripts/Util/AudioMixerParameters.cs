/// <summary>
/// Static class which stores the parameters used by the audio mixer.
/// </summary>
public static class AudioMixerParameters {

	/// <summary>
	/// Parameter for the master's volume.
	/// </summary>
	public static readonly string MasterVolume = "MasterVolume";
	
	/// <summary>
	/// Parameter for the music's volume.
	/// </summary>
	public static readonly string MusicVolume = "MusicVolume";

	/// <summary>
	/// Parameter for the ambient's volume.
	/// </summary>
	public static readonly string AmbientVolume = "AmbientVolume";

	/// <summary>
	/// Parameter for the effects's volume.
	/// </summary>
	public static readonly string EffectsVolume = "EffectsVolume";
}